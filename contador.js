let NumeroDeAlunos = 10

for (let contador = 0; contador <= NumeroDeAlunos; contador++){
    if (contador == 0){
        console.log["O numero atrual é zero"]
    }
    else if(contador % 2 == 0){
        console.log["O numero " + contador + " é PAR"]
    }
    else{
        console.log["O nuemro " + contador + "é IMPAR"]
    }
}